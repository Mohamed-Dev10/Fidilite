CREATE TABLE [dbo].[Cliente] (
    [Id]                   BIGINT         IDENTITY (1, 1) NOT NULL,
    [Code]                 NVARCHAR (255) NULL,
    [Nom]                  NVARCHAR (MAX) NOT NULL,
    [Prenom]               NVARCHAR (MAX) NOT NULL,
    [Cin]                  NVARCHAR (MAX) NULL,
    [NIA]                  DECIMAL (20)   NULL,
    [Email]                VARCHAR (255)  NULL,
    [Gsm]                  NVARCHAR (MAX) NOT NULL,
    [Adresse]              NVARCHAR (MAX) NULL,
    [CodeBarre]            NVARCHAR (MAX) NULL,
    [DateNaissance]        DATETIME       NOT NULL,
    [Fonction]             NVARCHAR (MAX) NULL,
    [RaisonSociale]        NVARCHAR (MAX) NULL,
    [REF_Genre_Id]         INT            NOT NULL,
    [REF_Ville_Id]         INT            NULL,
    [REF_Magasin_Id]       INT            NOT NULL,
    [REF_CarteType_Id]     INT            NULL,
    [REF_Metier_Id]        INT            NULL,
    [REF_ClienteStatut_Id] INT            NOT NULL,
    [IsConfirmed]          BIT            NULL,
    [IsActif]              BIT            NULL,
    [RemarqueDeactivation] NVARCHAR (MAX) NULL,
    [ProfilDeactivation]   NVARCHAR (450) NULL,
    [DateDeactivation]     DATETIME       NULL,
    [DateConfirmation]     DATETIME       NULL,
    [DateCreation]         DATETIME       NOT NULL,
    PRIMARY KEY CLUSTERED ([Id] ASC),
    CONSTRAINT [FK_Cliente_ToProfil] FOREIGN KEY ([ProfilDeactivation]) REFERENCES [dbo].[Profil] ([Id]),
    CONSTRAINT [FK_Cliente_ToREF_CarteType] FOREIGN KEY ([REF_CarteType_Id]) REFERENCES [dbo].[REF_CarteType] ([Id]),
    CONSTRAINT [FK_Cliente_ToREF_ClienteStatut] FOREIGN KEY ([REF_ClienteStatut_Id]) REFERENCES [dbo].[REF_ClienteStatut] ([Id]),
    CONSTRAINT [FK_Cliente_ToREF_Genre] FOREIGN KEY ([REF_Genre_Id]) REFERENCES [dbo].[REF_Genre] ([Id]),
    CONSTRAINT [FK_Cliente_ToREF_Magasin] FOREIGN KEY ([REF_Magasin_Id]) REFERENCES [dbo].[REF_Magasin] ([Id]),
    CONSTRAINT [FK_Cliente_ToREF_Metier] FOREIGN KEY ([REF_Metier_Id]) REFERENCES [dbo].[REF_Metier] ([Id]),
    CONSTRAINT [FK_Cliente_ToREF_Ville] FOREIGN KEY ([REF_Ville_Id]) REFERENCES [dbo].[REF_Ville] ([Id]),
    CONSTRAINT [UQ_Cliente_Email] UNIQUE NONCLUSTERED ([Email] ASC)
);
